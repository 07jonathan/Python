# Program demonstrasi casting di Python

# Casting ke integer
int_x = int(1)       # dari integer literal
int_y = int(2.8)     # dari float literal
int_z = int("3")     # dari string literal yang merepresentasikan angka bulat

print("Casting ke integer:")
print("int_x:", int_x)  # Output: 1
print("int_y:", int_y)  # Output: 2
print("int_z:", int_z)  # Output: 3

# Casting ke float
float_x = float(1)       # dari integer literal
float_y = float(2.8)     # dari float literal
float_z = float("3")     # dari string literal yang merepresentasikan angka
float_w = float("4.2")   # dari string literal yang merepresentasikan angka desimal

print("\nCasting ke float:")
print("float_x:", float_x)  # Output: 1.0
print("float_y:", float_y)  # Output: 2.8
print("float_z:", float_z)  # Output: 3.0
print("float_w:", float_w)  # Output: 4.2

# Casting ke string
str_x = str("s1")  # dari string literal
str_y = str(2)     # dari integer literal
str_z = str(3.0)   # dari float literal

print("\nCasting ke string:")
print("str_x:", str_x)  # Output: 's1'
print("str_y:", str_y)  # Output: '2'
print("str_z:", str_z)  # Output: '3.0'
